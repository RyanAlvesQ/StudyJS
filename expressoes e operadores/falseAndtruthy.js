// Falsy
console.log (0 ? 'true' : 'false')
console.log ("" ? 'true' : 'false')
console.log (null ? 'true' : 'false')
console.log (NaN ? 'true' : 'false')

console.log ("-------------------------------")

// Truthy
console.log (1 ? 'true' : 'false')
console.log ("1" ? 'true' : 'false')
console.log (Infinity ? 'true' : 'false')
console.log ([] ? 'true' : 'false')
console.log ({} ? 'true' : 'false')
